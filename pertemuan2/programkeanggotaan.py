#deklarasi
kalimat ="selamat datang"

#program operator keanggotaan
print('a in kalimat', 'a' in kalimat)
print('amat in kalimat', 'amat' in kalimat)
print('Selamat in kalimat', 'Selamat' in kalimat)

print('a not in kalimat', 'a' not in kalimat)
print('amat not in kalimat', 'amat' not in kalimat)
print('Selamat not in kalimat', 'Selamat' not in kalimat)